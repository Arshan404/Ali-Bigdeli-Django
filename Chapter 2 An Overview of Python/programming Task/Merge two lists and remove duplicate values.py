List_1 = [1, 2, 3, 4, 5]
List_2 = [4, 5, 6, 7, 8]

merged = List_1 + List_2
unique_list = list(set(merged))  # حذف تکراری‌ها
print(unique_list[:3])           ## نمایش 3 عضو اول